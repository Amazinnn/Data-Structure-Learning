/*
# P2840 纸币问题 2

## 题目描述
你有 $n$ 种面额互不相同的纸币，第 $i$ 种纸币的面额为 $a_i$ 并且有无限张，
现在你需要支付 $w$ 的金额，求问有多少种方式可以支付面额 $w$，答案对 $10^9+7$ 取模。  
注意在这里，同样的纸币组合如果支付顺序不同，会被视作不同的方式。
例如支付 $3$ 元，使用一张面值 $1$ 的纸币和一张面值 $2$ 的纸币会产生两种方式（$1+2$ 和 $2+1$）。

## 输入格式
第一行两个正整数 $n,w$，分别表示纸币的种数和要凑出的金额。  
第二行一行 $n$ 个以空格隔开的正整数 $a_1, a_2, \dots a_n $ 依次表示这 $n$ 种纸币的面额。

## 输出格式
一行一个整数，表示支付方式的数量。

## 说明/提示

对于 $40\%$ 的数据，满足 $n\le 10$，$w\le 100$；  
对于 $100\%$ 的数据，满足 $1\le n\le 10^3$，$1\le a_i , w\le 10^4$。

*/

#include <stdio.h>
#define MOD 1000000007  // 定义模数，用于结果取模，避免溢出并满足题目要求

int main(void)
{
    int n, m; // n: 纸币面额种类数, m: 目标金额
    scanf("%d %d", &n, &m);
    
    // 动态规划数组：f[i]表示凑出金额i的支付方式总数（考虑顺序）
    long long int f[m+1] = {0}; // 使用long long防止大数溢出
    int cost[n]; // 存储n种纸币的面额
    
    // 读取所有纸币面额
    for (int i = 0; i < n; i++) {
        scanf("%d", &cost[i]);
    }
    
    // 初始化动态规划基础情况：凑出金额0只有1种方式（不支付任何纸币）
    // 这个初始化至关重要，它是所有状态转移的起点[1,2](@ref)
    f[0] = 1;
    
    // 动态规划主循环：计算从1到m每个金额的支付方式数
    // 注意：此外层循环顺序使此题成为计算"排列数"（考虑支付顺序）[2,4](@ref)
    for (int i = 1; i <= m; i++) {
        // 遍历每种面额的纸币，考虑将其作为最后一张支付的纸币
        for (int j = 0; j < n; j++) {
            // 如果当前纸币面额不超过目标金额，可以考虑使用
            if (cost[j] <= i) {
                // 状态转移方程：当前方式数 = 原有方式数 + 使用当前面额前的方案数
                // f[i-cost[j]]表示支付i-cost[j]元的所有方式，加上cost[j]面额纸币即得i元[2](@ref)
                f[i] = (f[i] + f[i - cost[j]]) % MOD; // 每次加法后取模防止溢出
            }
        }
    }
    
    // 输出结果：凑出金额m的所有支付方式数
    printf("%lld", f[m]);
    return 0;
}

























