/*
# P1048 [NOIP 2005 普及组] 采药

## 题目描述
辰辰是个天资聪颖的孩子，他的梦想是成为世界上最伟大的医师。
为此，他想拜附近最有威望的医师为师。医师为了判断他的资质，给他出了一个难题。
医师把他带到一个到处都是草药的山洞里对他说：“孩子，这个山洞里有一些不同的草药，
采每一株都需要一些时间，每一株也有它自身的价值。我会给你一段时间，
在这段时间里，你可以采到一些草药。
如果你是一个聪明的孩子，你应该可以让采到的草药的总价值最大。”
如果你是辰辰，你能完成这个任务吗？

## 输入格式
第一行有 $2$ 个整数 $T$（$1 \le T \le 1000$）和 $M$（$1 \le  M \le 100$），
用一个空格隔开，$T$ 代表总共能够用来采药的时间，$M$ 代表山洞里的草药的数目。
接下来的 $M$ 行每行包括两个在 $1$ 到 $100$ 之间（包括 $1$ 和 $100$）的整数，
分别表示采摘某株草药的时间和这株草药的价值。

## 输出格式
输出在规定的时间内可以采到的草药的最大总价值。

**【数据范围】**
- 对于 $30\%$ 的数据，$M \le 10$；
- 对于全部的数据，$M \le 100$。

**【题目来源】**
NOIP 2005 普及组第三题
*/
#include <stdio.h>
#define Max(a,b) ((a)>(b)?(a):(b))  // 定义求最大值的宏

int main(void)
{
    int t, m; // t: 总时间，m: 草药数量
    scanf("%d %d", &t, &m);
    // herb数组：herb[i][0]存储采摘时间，herb[i][1]存储草药价值（索引从1开始使用）
    int herb[m+1][2];
    for (int i = 1; i <= m; i++) 
        scanf("%d %d", &herb[i][0], &herb[i][1]);
    // DP数组：f[i][j]表示考虑前i株草药，在时间j内能获得的最大价值[1,5](@ref)
    long int f[m+1][t+1];
    
    // 初始化DP数组：所有值设为0[6](@ref)
    for (int i = 0; i <= m; i++) {
        for (int j = 0; j <= t; j++) {
            f[i][j] = 0;
        }
    }
    
    // 动态规划核心：外层遍历草药种类，内层遍历所有可能的时间[4,7](@ref)
    for (int type = 1; type <= m; type++) {
        for (int time = 0; time <= t; time++) {
            if (time >= herb[type][0]) {
                // 时间充足：选择采摘或不采摘中的最大值[8](@ref)
                // f[type-1][time]: 不采摘当前草药，继承前type-1株草药的最优解
                // f[type-1][time-herb[type][0]] + herb[type][1]: 采摘当前草药
                f[type][time] = Max(f[type-1][time], 
                                   f[type-1][time - herb[type][0]] + herb[type][1]);
            }
            else {
                // 时间不足：无法采摘当前草药，直接继承前type-1株草药的最优解[1](@ref)
                f[type][time] = f[type-1][time];
            }
        }
    }
    
    // 输出结果：使用所有m株草药，在时间t内能获得的最大价值[5](@ref)
    printf("%ld", f[m][t]);
    return 0;
}

























